extension CoreDataManager {
    /// 목업 상품 데이터 주입
    func seedMockProductsIfNeeded() throws {
        if UserDefaults.standard.bool(forKey: ProductMockSeed.userDefaultsKey) {
            return
        }

        if try fetchAllProducts().isEmpty == false {
            UserDefaults.standard.set(true, forKey: ProductMockSeed.userDefaultsKey)
            return
        }

        try ProductMockSeed.payloads.forEach { payload in
            try createProduct(payload)
        }

        UserDefaults.standard.set(true, forKey: ProductMockSeed.userDefaultsKey)
    }
